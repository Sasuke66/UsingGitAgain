class first{
  public static void main(String[] args){
    String a = "Satwik Raj ";
    String b = "Yadav";
    String c = a + b; 
    int x = 19;
    System.out.println(c);
    System.out.println(x);
    //boolean data type...
    boolean javaGood = true;
    boolean IamMad = false;
    System.out.println(javaGood);
    System.out.println(IamMad);
    /*type casting... 
    1) Widening casting...*/
    int s = 1;
    double t = s;
    System.out.println(s);
    System.out.println(t);
    /*2) Narrowing casting... */
    double f = 9.34563;
    int g = (int) f;
    System.out.println(f);
    System.out.println(g);
  }
}